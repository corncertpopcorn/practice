package song;

public class Person {
	private String name;
	private int age;
	private String job;
	
	
	
	public String get_name() {
		return this.name;
	}
	
	public void set_name(String name) {
		this.name = name;
	}
	
	public int get_age() {
		return this.age;
	}
	
	public void set_age(int age) {
		this.age = age;
	}
	
	public String get_job() {
		return this.job;
	}
	
	public void set_job(String job) {
		this.job = job;	
	}
	
	
}
